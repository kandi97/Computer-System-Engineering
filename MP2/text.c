/*
 * added by Sean 10/07/2017
 * text_to_graph
 *     DESCRIPTION: Convert the received text into graph which will be stored at the buffer from the input
 *     INPUTS: buf_statusBar is the buffer pointer which points to the buffer which stores the graph of status bar
 *				statusMsg is the string of the message
 *				inputText is the string of the input typed text
 *				placeName is the string of the name of the current place
 *     OUTPUTS: none
 *     RETURN VALUE: none
 *     SIDE EFFECTS: put the graph made from the text into the buf_statusBar
 */
 void text_to_graph(unsigned char *buf_statusBar, char *statusMsg, const char *inputText, const char *placeName)
 {
	 int i, j, k;
	
	/* Create an string with size 41 (last byte store null as flag)*/
	unsigned char result[MSG_LEN+1];
	result[MSG_LEN] = '\0';
	
	/* Clear the result to blank first */
	for(i=0;i<MSG_LEN;i++)
		result[i] = ' ';
	
	/* Check if the status bar should show the message*/
	/* If it is not currently showing message, put the "placeName" and "input" into result */
	if(statusMsg[0] == '\0')
	{
		/* Input the placeName into result first */
		for(i=0;i<strlen(placeName);i++)
			result[i] = placeName[i];
		
		/* The rightest character will be '_' first */
		result[MSG_LEN-1] = '_';
		
		/* If the inputText is less than 20 characters, the '_' should be remained */
		if(strlen(inputText) < INPUT_TEXT_MAX)
			/* Input the text into result*/
			for(i=0;i<=strlen(inputText);i++)		
				result[MSG_LEN-2 - i] = inputText[strlen(inputText)-1 - i];		//-1 since there's a '\0'
		/* If the inputText is 20 characters, the '_' should be disappeared */
		else
			/* Input the text into result*/
			for(i=0;i<INPUT_TEXT_MAX;i++)		
				result[MSG_LEN-1 - i] = inputText[strlen(inputText)-1 - i];		//-1 since there's a '\0'
	}
	/* If it is currently showing message, put the "statusMsg" into result */
	else
	{
		/* In order to put the message in the middle of the screen, calculate the start of iteration first */
		int iter_start = MSG_LEN/2-strlen(statusMsg)/2;
		for(i=iter_start;i<iter_start+strlen(statusMsg);i++)
			result[i] = statusMsg[i-iter_start];
	}
	
	/* Set the entire status bar to black first */
	memset(buf_statusBar, BACKGROUND_COLOR, STATUSBAR_ADDR_SIZE*4); 	//*4 since there are 4 planes
	
	/* After storing the text into result, convert them into graph */
	for(i=0;i<MSG_LEN;i++)
	{
		/* Store the currently processing charater to "currentChar" */
		int currentChar = result[i];
		
		/* Since each character has 16 data stored in font_data, go through each data to find which one suits */
		/* 16 is height */
		for(j=0;j<16;j++)
		{
			/* Use bitMask to retrieve data from font_data (0x80 => 1000 0000)*/
			unsigned char bitMask = 0x80;
			
			/* Go through 8 bits and find for the current character in font_data */
			for(k=0;k<8;k++)
			{
				/* If any certain data has the same bit as bitMask, put pixel into corresponding place in buf_statusBar */
				if(bitMask & font_data[currentChar][j])
				{
					/* Should deal with 4 planes */
					/* Add the plane offset first, and one pixel height above the text */
					/* then j*X_WIDTH to find out which row */
					/* finally, add the number of the order to make sure the pixel is put on the correct position */
					if(k<4)
						buf_statusBar[(k%4)*STATUSBAR_ADDR_SIZE + X_WIDTH + (X_WIDTH*j) + 2*i] = FONT_COLOR;
					else
						buf_statusBar[(k%4)*STATUSBAR_ADDR_SIZE + X_WIDTH + (X_WIDTH*j) + 2*i + 1] = FONT_COLOR;
		
				}
				/* bitMask shifts right by 1 bit */
				bitMask >>= 1;
			}
		}
	}
 } 
